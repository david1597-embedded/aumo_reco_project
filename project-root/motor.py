class MecanumMotor:
    def move_forward(self):
        print("[MOTOR] Moving forward")

    def move_backward(self):
        print("[MOTOR] Moving backward")

    def rotate(self):
        print("[MOTOR] Rotating")

    def call(self):
        print("[MOTOR] Calling")

    def follow(self):
        print("[MOTOR] Following")

    def stop(self):
        print("[MOTOR] Stopping")
